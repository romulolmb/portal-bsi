<h2>OBJETIVOS DA DISCIPLINA</h2>
<p>Estas disciplinas englobam as diversas atividades realizadas por todos os alunos do Bacharelado em Sistemas de Informação, com vistas a sua formação complementar. Visam cumprir a Meta 12, Estratégia 12.7, do Plano Nacional de Educação (Lei No 12.7) assegurar, no mínimo, 10% (dez por cento) do total de créditos curriculares exigidos para a graduação em programas e projetos de extensão universitária, orientando sua ação, prioritariamente, para áreas de grande pertinência social;</p>

<h2>EMENTA</h2>
<p>Atividades de extensão universitária, compreendendo: ações comunitárias, estágios internos e externos, participação em projetos de extensão, e outras modalidades de formação complementar apresentadas na Resolução No 2628, de 08/09/2005 que dispõe sobre a regulamentação das Atividades Complementares nos currículos dos Cursos de Graduação da UNIRIO.</p>

<h2>CONTEÚDO PROGRAMÁTICO</h2>
<p>As ACE incluem estágios supervisionados, programas de trainees, monitorias, bolsas, cursos 

extracurriculares, atuação profissional em área afim ao curso, representação discente em Diretórios 

Acadêmicos, Colegiados de Escolas e Departamentos ou Conselhos do Centro e da Universidade, 

participação em projetos de pesquisa ou extensão ou na organização de eventos, bem como colaboração 

em Empresa Júnior, quer como consultores independentes quer como membros da direção. As atividades 

elegíveis para ACE estão previstas na Resolução UNIRIO No 2.628, de 8 de setembro de 2005. 

O Colegiado do Curso de Bacharelado em Sistemas de Informação, em reunião realizada em 19 de 

junho de 2015, deliberou que, no mínimo, duas disciplinas ACE devem ser cumpridas com atividade 

acadêmica de pesquisa ou extensão, estágio ou trabalho na área de Sistemas de Informação ou em áreas 

afins.</p>

<h2>METODOLOGIA</h2>
<p>Aprendizagem baseada em desenvolvimento de atividades.

Encontros presenciais servem para a apresentação da disciplina, como pontos de controle no 

cronograma de desenvolvimento das atividades e para a apresentação das atividades desenvolvidas pelos 

alunos. O controle é feito através da entrega de artefatos intermediários, a saber: 
<ul>
	<li> Questionário individual sobre a atividade 
	<li> Documentação comprobatória da atividade 
	<li> Relatório de avaliação da chefia imediata 
	<li> Relatório final da atividade 
</ul>
O relatório final deve mostrar, numa visão global, todas as tarefas realizadas pelo aluno no 

desenvolvimento de suas atividades, as disciplinas do curso que estão sendo relevantes, as dificuldades 

encontradas e sugestões para melhoria do desempenho próprio e do curso.</p>

<h2>AVALIAÇÃO</h2>
<p>Formativa: os alunos entregam artefatos intermediários segundo um cronograma, apresentam suasatividades nos encontros presenciais e, ao final da disciplina, entregam o relatório final.
<br>A avaliação é feita através de conceito, mediante o cumprimento integral das entregas de artefatos: S (suficiente) ou I (insuficiente).</p>

