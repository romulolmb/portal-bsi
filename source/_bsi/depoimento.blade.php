	<p>“Por que Sistemas de Informação?”
	Ouvi isso lá em 2009, na primeira aula de Fundamentos de Sistemas de Informação. Não lembro muito bem a minha resposta, mas foi algo como “gosto de computadores”. Me lembro daquela primeira aula como se fosse ontem, e lembro também o quanto pensei antes de dar aquela resposta, tentando não “responder errado”. Hoje percebo que algumas perguntas simplesmente não possuem respostas certas. E se alguém me fizer essa mesma pergunta agora, eu com certeza terei muito mais o que dizer do que jamais conseguiria pensar naquela tarde. Mas e aí, como condensar quatro anos e meio em uma só frase? A Unirio me deu muito mais que um diploma (que ainda está pendente, haha): me deu amigos, me deu experiências, me deu lembranças, e isso é o tipo de coisa que você não pode prever e muito menos encurtar para caber em uma só frase. E essas são as coisas que realmente importam.</p>

	<p>Muito obrigada a todos os envolvidos direta ou indiretamente nesta jornada, todos vocês me ensinaram muito. Muito mais que códigos, conceitos ou números. Muito mais que respostas. Aprendi a questionar. Aprendi que a aprendizagem não é um processo passivo, e que as perguntas são tão importantes quanto (ou mais que) as respostas.</p>

