<h1>Histórico</h1>

<p>O curso Bacharelado em Sistemas de Informação foi oferecido pela primeira vez no 2<sup>o</sup> semestre de 2000, com a primeira turma composta de 30 alunos. Ao final de 2004, o curso formou a sua primeira turma com nove graduados. Foi o primeiro curso vespertino/noturno de graduação em Informática em Universidades Públicas do Estado do Rio de Janeiro.</p>

<p> O curso foi criado por uma Comissão Multidisciplinar designada pela Portaria n<sup>o</sup> 277, de 2 de julho de 1998, que elaborou o seu <a href="projeto_academico_inicial.pdf">Projeto Acadêmico</a>, e obteve aprovação pela 
<a href="resolucao_2025.pdf">Resolução UNIRIO n<sup>o</sup> 2.025</a> de 12 de abril de 1999. Através da <a href="resolucao_2355.pdf">Resolução UNIRIO n<sup>o</sup> 2.355</a>, de 30 de abril de 2002, o curso de Bacharelado em Informática passou a ser denominado Bacharelado em Sistemas de Informação (BSI), denominação compatível com as Diretrizes Curriculares da Área de Computação e Informática – MEC/SESu.</p>

<p>O curso de BSI foi reconhecido pela <a href="portaria_3799.pdf">Portaria MEC n<sup>o</sup> 3.799</a>, de 17 de novembro de 2004, publicada no Diário Oficial da União de 18 de novembro de 2004, após <a href="avaliacao_inep.pdf">avaliação por Comissão do MEC</a>, realizada em maio de 2004. Em 2007, foi feita uma reforma curricular e o novo <a href="PPC BSI UNIRIO Ajustado LIBRAS 2012.pdf">Projeto Acadêmico</a> entrou em vigor a partir do 1o semestre de 2008.</p>

